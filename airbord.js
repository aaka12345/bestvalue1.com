function show(){
    var a=document.getElementById("nav");
    a.style.transform="translateX(0px)";
    var b=document.getElementById("sqar");
    b.style.display="none";
    var c=document.getElementById("X");
    c.style.display="block";
    }
    function hide(){
    var a=document.getElementById("nav");
    a.style.transform="translateX(-250px)";
    var b=document.getElementById("X");
    b.style.display="none";
    var c=document.getElementById("sqar");
    c.style.display="block";
    }
    